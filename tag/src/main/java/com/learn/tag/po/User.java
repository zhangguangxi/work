package com.learn.tag.po;

/**
 * User实体类
 * 
 * @version 2015年12月10日上午12:30:45
 * @author guangxi.zhang
 */
public class User {
    /** 主键id(自增) */
    private Integer id;
    /** 用户名 */
    private String name;
    
    /**
     * @return the id
     */
    public Integer getId() {
        return id;
    }
    
    /**
     * @param id the id to set
     */
    public void setId(Integer id) {
        this.id = id;
    }
    
    /**
     * @return the name
     */
    public String getName() {
        return name;
    }
    
    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }
    
}
